def expand_query(query):
    """
    Complete query expansion pipeline:
    1. Spell correction
    2. Synonym expansion
    3. LLM expansion (if enabled)
    
    Args:
        query: Original query string
        
    Returns:
        Set of expanded query terms
    """
    # Split query into words
    words = query.lower().split()
    
    # Step 1: Spell correction
    if ENABLE_SPELL_CORRECTION:
        corrected_words = [correct_spelling(word) for word in words]
        logger.debug(f"After spell correction: {corrected_words}")
    else:
        corrected_words = words
    
    # Step 2: Synonym expansion
    expanded_words = expand_with_synonyms(corrected_words)
    logger.debug(f"After synonym expansion: {expanded_words}")
    
    # Step 3: LLM expansion
    if ENABLE_LLM_EXPANSION:
        llm_terms = llm_expand_query(query)
        expanded_words.update(llm_terms)
        logger.debug(f"After LLM expansion: {expanded_words}")
    
    logger.info(f"Query expansion: '{query}' -> {expanded_words}")
    return expanded_words


