"""
Query expansion module with spell correction, synonym expansion, and LLM support.
"""

import logging
from spellchecker import SpellChecker
from config import (
    MATH_SYNONYMS, 
    ENABLE_SPELL_CORRECTION, 
    ENABLE_SYNONYM_EXPANSION,
    ENABLE_LLM_EXPANSION
)

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Initialize spell checker (lazy loading)
_spell_checker = None


def get_spell_checker():
    """Get or initialize the spell checker."""
    global _spell_checker
    if _spell_checker is None:
        _spell_checker = SpellChecker()
        # Add math-specific words to dictionary
        math_words = set([
            'calculus', 'algebra', 'geometry', 'trigonometry', 'topology',
            'differential', 'integral', 'derivative', 'matrix', 'vector',
            'theorem', 'lemma', 'proof', 'axiom', 'corollary', 'polynomial',
            'eigenvalue', 'eigenvector', 'determinant', 'fourier', 'laplace',
            'euclidean', 'gaussian', 'riemann', 'hilbert', 'banach', 'cauchy'
        ])
        _spell_checker.word_frequency.load_words(math_words)
    return _spell_checker


def correct_spelling(word):
    """
    Correct spelling of a single word.
    
    Args:
        word: Input word (string)
        
    Returns:
        Corrected word (string)
    """
    if not ENABLE_SPELL_CORRECTION:
        return word
    
    try:
        spell = get_spell_checker()
        
        # If word is in dictionary, return as-is
        if word in spell:
            return word
        
        # Get correction
        correction = spell.correction(word)
        
        # Only return correction if it's different and confident
        if correction and correction != word:
            logger.debug(f"Spell correction: {word} -> {correction}")
            return correction
        
        return word
        
    except Exception as e:
        logger.warning(f"Spell check error for '{word}': {e}")
        return word


def expand_with_synonyms(words):
    """
    Expand query words with synonyms from the math dictionary.
    
    Args:
        words: List of query words
        
    Returns:
        Set of words including originals and synonyms
    """
    if not ENABLE_SYNONYM_EXPANSION:
        return set(words)
    
    expanded = set(words)
    
    for word in words:
        # Check for exact match
        if word in MATH_SYNONYMS:
            synonyms = MATH_SYNONYMS[word]
            expanded.update(synonyms)
            logger.debug(f"Synonyms for '{word}': {synonyms}")
        
        # Check for multi-word phrases
        for phrase, synonyms in MATH_SYNONYMS.items():
            if ' ' in phrase:  # Multi-word phrase
                # Simple check if phrase appears in query
                query_str = ' '.join(words)
                if phrase in query_str:
                    expanded.update(synonyms)
                    logger.debug(f"Phrase synonyms for '{phrase}': {synonyms}")
    
    return expanded


